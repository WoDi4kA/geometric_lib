import math


def area(r):
    '''Принимает число r, возвращает умножение на число r и на число math.pi, возвращает площадь'''
    return math.pi * r * r


def perimeter(r):
    '''Принимает число r, возвращает умноженние на два м на число math.pi, возвращает периметр'''
    return 2 * math.pi * r

